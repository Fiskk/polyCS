<h2>Secured Wireless Connectivity:</h2>

<h3>Where is wireless available?</h3>
<p>A 802.11g wireless access point is currently located in Kunsela C122 which covers most
of the Kunsela classroom wing.</p>

<h3>What do I need?</h3>
<p>
1. An active SUNYIT Computer Science Account<br>
2. A wireless network adapter compatible with 802.11b or 802.11g<br>
3. A laptop computer running Windows XP Service Pack 2 with the latest updates<br>
</p>

