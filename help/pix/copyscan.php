<h3>How to Doc for scanning documents with the C128 copier directly to PDF documents:</h3>

<P>Follow these steps to scan your document from C128 copier.</P>

<P>You can download a copy and install the scanner drivers directly from our website</p>
<p><a 
href="http://www.cs.sunyit.edu/network/downloads/printers/"> </p>Install scanner drivers for C128 copier</a>.

<p>After you download and install the Scanner drivers, follow the steps below</p>

<p align="center">
<TABLE width=600>
<TR>
<TD valign="top"><p>Download and install the scanner drivers 
<TD><a href="screenshot1.jpg"><img height=100 width=100 
src="screenshot1.jpg"></a></TD>
</TR>

<TR>
<TD valign="top"><p>Click Start, Go to programs, then go to publishing, then Network ScanGear and click ScanGear Tool</p></TD>
<TD><a href="screenshot2.jpg"><img height=100 width=100 src="screenshot2.jpg"></a></TD>
</TR>

<TR>
<TD valign="top"><p>Configure the ScanGear Tool with the copy machine's IP Address and test the connection</p></TD>
<TD><a href="screenshot3.jpg"><img height=100 width=100 src="screenshot3.jpg"></a></TD>
</TR>

<TR>
<TD valign="top"><p>Go to the copier machine and enable the scanning function. Touch scan.</p></TD>
<TD><a href="image1.jpg"><img 
height=100 width=100 src="image1.jpg"></a></TD>
</TR>

<TR>
<TD valign="top"><p>Once at the scan screen touch online.</p></TD>              
<TD><a href="image2.jpg"><img
height=100 width=100 src="image2.jpg"></a></TD>
</TR>

<TR>
<TD valign="top"><p>Open Adobe Acrobat 8 Professional</p></TD>
<TD><a href="screenshot5.jpg"><img height=100 width=100 src="screenshot5.jpg"></a></TD>
</TR>

<TR>
<TD valign="top"><p>In Adobe Acrobat 8 Professional Click File, Create PDF, From Scanner.</p></TD>
<TD><a href="screenshot6.jpg"><img height=100 width=100 src="screenshot6.jpg"></a><TD>
</TR>

<TR>
<TD valign="top"><p>Make sure Network ScanGear Ver.2.21 is chosen then click Scan.</p></TD>
<TD><a href="screenshot7.jpg"><img height=100 width=100 src="screenshot7.jpg"></a></TD>
</TR>

<TR>
<TD valign="top"><p>Name your .pdf file and save.</p></TD>
<TD><a href="screenshot8.jpg"><img height=100 width=100 src="screenshot8.jpg"></a></TD>
</TR>

<TR>
<TD valign="top"><p>If you're done then click Cancel.</p></TD>
<TD><a href="screenshot9.jpg"><img height=100 width=100 src="screenshot9.jpg"></a></TD>    
</TR>

<TR>
<TD valign="top"><p>Choose Scanning complete if you're done then Click Ok.</p></TD>
<TD><a href="screenshot10.jpg"><img height=100 width=100 src="screenshot10.jpg"></a></TD>
</TR>

<TR>
<TD valign="top"><p>Go back to the copying machine in C128. Touch offline then touch copy to reset it for the next user.</p></TD>
<TD><a href="image4.jpg"><img height=100 width=100 src="image4.jpg"></a></TD>
</TR>






