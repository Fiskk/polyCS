<h3>Frontpage Webs:</h3>

<p>Frontpage webs are available for users who would like to maintain a website
using Microsoft Frontpage.  The URL for frontpage webs is in the form of 
http://turing.cs.sunyit.edu/<i>webname</i>, where <i>webname</i> can be 
anything you would like.</p>

<p>To request a frontpage web, fill out the
<a href="http://accounts.cs.sunyit.edu/fp-request.php">Frontpage Request form</a>.</p>

<p>You must first have an 
<a href="https://accounts.cs.sunyit.edu/activate.php">active account</a> on the Computer 
Science Dept network.</p>

<p>
To connect to your Frontpage Web:</p>
<UL type=circle>
<LI>You will need an account on the Computer Science Department Network.  If you have not already done so, 
<a href="https://accounts.cs.sunyit.edu/activate.php">Activate your account</a>.

<LI>After you activate your account, you can then log into the lab machines in c014 using your username
and password.  Log off of the public account, and log in as you.

<LI>Start Frontpage
<LI>Click on File/Open web

<LI>In the Web Name box, enter http://turing.cs.sunyit.edu/yourwebname, where  'yourwebname' is the name of the 
web that has been created for you.
<LI>If you are logged in to a CS lab machine using your account, you should not be prompted for a password.  
If you are trying to connect from home, enter your username and password, and the domain cs.sunyit.edu.

</UL>

